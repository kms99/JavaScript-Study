// falsy값, truthy 값
// 다른 타입들이 boolean 값으로 형 변환 하였을 때 false의 값을 가지는 값과 true의 값을 가지는 값

// falsy Test
if (0){
    console.log("hello");
}

if (""){
    console.log("hello");
}

if (null){
    console.log("hello");
}

if (undefined){
    console.log("hello");
}

if (NaN){
    console.log("hello");
}

if (false){
    console.log("hello");
}

if (true){
    console.log("hello"); // 실행
}